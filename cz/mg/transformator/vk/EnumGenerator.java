    private void generateComplex(){
        String input = jTextAreaInput.getText();
        String output = readAll();
        output = output.replace("NAME", genName(input));
        output = output.replace("PROPERTIES", genProperties(input));
        jTextAreaOutput.setText(output);
    }
    
    private String genName(String input){
        String[] lines = input.split("\\n");
        return lines[0].split(" ")[2];
    }
    
    private String genProperties(String input){
        String[] lines = input.split("\\n");
        String properties = "";
        for(int i = 1; i < lines.length - 1; i++){
            String property = "public static final int " + lines[i].trim().replace(",", "") + ";";
            properties = properties + "    " + property + "\n";
        }
        return properties.substring(0, properties.length()-1);
    }
    
    private String readAll(){
        InputStream stream = getClass().getResourceAsStream("EnumSample.txt");
        try(BufferedReader reader = new BufferedReader(new InputStreamReader(stream))){
            String line;
            String content = "";
            while((line = reader.readLine()) != null){
                content = content + line + "\n";
            }
            return content;
        } catch (IOException ex) {
            throw new RuntimeException();
        }
    }

